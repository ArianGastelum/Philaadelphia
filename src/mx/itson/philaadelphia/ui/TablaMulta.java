     private void cargarTabla(){
        
        try{
            
            DefaultTableModel modelo = (DefaultTableModel) tblMultas.getModel();
            modelo.setRowCount(0);
            MultaDAO con = new MultaDAO();

            List<Multa> cond = con.obtenerTodos();
            
            for(Multa d : cond){

                modelo.addRow(new Object[] {d.getId(),d.getFolio(),d.getMotivo(),d.getFecha(),d.getConductor().getNombre(),d.getOficial().getNombre()});

            }
            
        }catch(Exception ex){
            
            System.err.println("Ocurrio un error: " + ex.getMessage());
            
        }
    }                      
