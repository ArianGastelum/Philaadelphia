    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
    String folio = txtFolio.getText();
Date fecha = new Date();
String motivo = txtMotivo.getText();
Conductor conductor = (Conductor) cbConductor.getSelectedItem();
Oficial oficial = (Oficial) cbOficial.getSelectedItem();
Multa multa = new Multa(folio, fecha, motivo, conductor, oficial);

try {
    MultaDAO.guardarMulta(folio, motivo, fecha, conductor, oficial);
    JOptionPane.showMessageDialog(this, "Registro exitoso");
    dispose();
} catch (Exception ex) {
    JOptionPane.showMessageDialog(this, "Registro fallido: " + ex.getMessage());
}

    }//GEN-LAST:event_btnAgregarActionPerformed

